    <h1 class="text-center mb-4">5 Alasan Mengapa Bali Selalu Menarik untuk Dikunjungi</h1>
    <hr>

    <img src="styles/picture/Sunset-di-Kuta-Bali.jpg" class="img-fluid rounded mb-4" alt="Pantai di Bali">

    <p class="text-muted fst-italic">Diposting pada 20 Agustus 2025</p>

    <div class="article-content">
      <p>Bali, atau yang sering dijuluki "Pulau Dewata", adalah salah satu destinasi wisata paling populer di dunia.
        Tidak hanya menawarkan keindahan alam yang memukau, Bali juga kaya akan budaya dan keramahan penduduknya.
        Berikut adalah beberapa alasan mengapa Bali menjadi tempat yang sempurna untuk liburan Anda.</p>

      <h5>1. Keindahan Alam yang Tak Ada Duanya</h5>
      <p>Dari pantai-pantai berpasir putih seperti Kuta dan Seminyak, hingga sawah terasering yang hijau di Ubud, Bali
        memiliki pemandangan alam yang sangat beragam. Jangan lewatkan kesempatan untuk menyaksikan matahari terbit di
        Gunung Batur atau menikmati keindahan air terjun tersembunyi seperti Tegenungan.</p>

      <h5>2. Budaya dan Seni yang Hidup</h5>
      <p>Budaya Bali sangat kental dan bisa dirasakan di setiap sudut pulau. Anda dapat menyaksikan pertunjukan tari
        tradisional, mengunjungi pura-pura megah seperti Pura Tanah Lot dan Pura Uluwatu, atau mengikuti kelas membuat
        kerajinan tangan dan melukis.</p>

      <h5>3. Surga Para Pecinta Kuliner</h5>
      <p>Kuliner Bali menawarkan pengalaman rasa yang unik, dari hidangan tradisional seperti Babi Guling dan Ayam
        Betutu, hingga kafe-kafe modern dengan menu internasional yang lezat. Ada banyak pilihan makanan yang sesuai
        untuk setiap selera dan anggaran.</p>
